# Create basic WAV files for MonoGame
$contentPath = "Core\Content"

# Create a minimal WAV file (44 bytes - silent 1 second at 8kHz mono)
$wavBytes = @(
    0x52, 0x49, 0x46, 0x46, 0x24, 0x00, 0x00, 0x00, 0x57, 0x41, 0x56, 0x45, 0x66, 0x6D, 0x74, 0x20,
    0x10, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x40, 0x1F, 0x00, 0x00, 0x40, 0x1F, 0x00, 0x00,
    0x01, 0x00, 0x08, 0x00, 0x64, 0x61, 0x74, 0x61, 0x00, 0x00, 0x00, 0x00
)

# Create audio files
$audioFiles = @(
    "door_open.wav",
    "panel_beep.wav", 
    "step_hallway.wav",
    "whisper_loop.wav"
)

foreach ($audio in $audioFiles) {
    $path = Join-Path $contentPath $audio
    [System.IO.File]::WriteAllBytes($path, $wavBytes)
    Write-Host "Created: $path"
}

Write-Host "Basic audio files created successfully!"